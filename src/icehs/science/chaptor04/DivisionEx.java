package icehs.science.chaptor04;

public class DivisionEx {

	public static void main(String[] args) {
		int number1 = 10;
		int number2 = 3;
		
		int divisionResult = number1 / number2;
		int remainderResult = number1 % number2;
		
		System.out.println(number1 + " / " + number2);
		System.out.println("나눗셈의 결과 :" + divisionResult);
		System.out.println("나머지 :" + remainderResult);	
	}

}
